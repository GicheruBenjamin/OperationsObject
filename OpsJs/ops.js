
// An object containing methods for doing operations

const ops = {
    add: function(a, b) {
      return a + b;
    },
    subtract: function(a, b) {
      return a - b;
    },
    multiply: function(a, b) {
      return a * b;
    },
    division: function(a, b) {
      return a / b;
    }
  };
  
  module.exports = ops;