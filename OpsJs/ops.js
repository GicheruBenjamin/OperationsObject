
// An object containing methods for doing operations
// operations.js

const operations = {
    plus: function(a, b) {
      return a + b;
    },
    minus: function(a, b) {
      return a - b;
    },
    dot: function(a, b) {
      return a * b;
    },
    star: function(a, b) {
      return a / b;
    }
  };
  
  module.exports = operations;